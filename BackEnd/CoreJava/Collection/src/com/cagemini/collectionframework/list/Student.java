package com.cagemini.collectionframework.list;

public class Student 
{
	String name;
	int id;
	double height;
	double percentage;
	int age;
	
	public Student(String name, int id, double height, double percentage, int age) {
		super();
		this.name = name;
		this.id = id;
		this.height = height;
		this.percentage = percentage;
		this.age = age;
	}
}
